var a = document.getElementById("hotimgs1");

a.addEventListener("mouseover", () => {
    a.style.transform = "scale(1.1)";
    a.style.transitionDuration = "2s";
});
a.addEventListener("mouseout", () => {
    a.style.transform = "scale(1)";
    a.style.transitionDuration = "2s";
});

